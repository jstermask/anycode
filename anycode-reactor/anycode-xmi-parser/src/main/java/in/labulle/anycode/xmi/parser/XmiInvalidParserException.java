package in.labulle.anycode.xmi.parser;

public class XmiInvalidParserException extends XmiParserException {

	/**
	 * uid.
	 */
	private static final long serialVersionUID = -2008773433147421791L;

	public XmiInvalidParserException() {
		super();
		
	}

	public XmiInvalidParserException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public XmiInvalidParserException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public XmiInvalidParserException(String message) {
		super(message);
		
	}

	public XmiInvalidParserException(Throwable cause) {
		super(cause);
		
	}

}
