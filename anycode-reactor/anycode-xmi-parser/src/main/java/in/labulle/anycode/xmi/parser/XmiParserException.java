package in.labulle.anycode.xmi.parser;

public class XmiParserException extends RuntimeException {

	public XmiParserException() {
		super();

	}

	public XmiParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public XmiParserException(String message, Throwable cause) {
		super(message, cause);

	}

	public XmiParserException(String message) {
		super(message);

	}

	public XmiParserException(Throwable cause) {
		super(cause);

	}

	/**
	 * uid.
	 */
	private static final long serialVersionUID = 6454844370181538490L;

}
